package Entidades;

/**
 *
 * @author Dani Lucano
 */
public class Nivel {
    int nivel_id;
    String nivel;
    
    public Nivel(int nivel_id, String nivel) {
		super();
		this.nivel_id = nivel_id;
		this.nivel = nivel;
	}

	public int getNivel_id() {
        return nivel_id;
    }

    public void setNivel_id(int nivel_id) {
        this.nivel_id = nivel_id;
    }

    public String getNivel() {
        return nivel;
    }

    public void setNivel(String nivel) {
        this.nivel = nivel;
    }
    
    
}
