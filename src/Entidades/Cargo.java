package Entidades;

/**
 *
 * @author Dani Lucano
 */
public class Cargo {
    int cargo_id;
    String nombre;

    public Cargo(int cargo_id, String nombre) {
		super();
		this.cargo_id = cargo_id;
		this.nombre = nombre;
	}

	public int getCargo_id() {
        return cargo_id;
    }

    public void setCargo_id(int cargo_id) {
        this.cargo_id = cargo_id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
}
